// ??$$$ Simplified callGeminiIdeation (Agent 1 - Interviewer, no tools, no projectId parameter)
export const callGeminiIdeation = async (
  systemPrompt: string,
  messages: any[]
): Promise<string> => {
  const geminiApiKey = process.env.GEMINI_API_KEY;

  if (geminiApiKey) {
    try {
      const genAI = new GoogleGenerativeAI(geminiApiKey);
      const model = genAI.getGenerativeModel({
        model: "gemini-2.5-flash",
        systemInstruction: systemPrompt,
      });

      // Map messages history to Gemini format
      const history = messages.slice(0, -1).map((m: any) => ({
        role: m.role === "model" || m.role === "ai" || m.role === "assistant" ? "model" : "user",
        parts: [{ text: m.content || "" }]
      }));

      const lastMessage = messages[messages.length - 1];
      const chat = model.startChat({ history });
      const result = await chat.sendMessage(lastMessage.content || "");
      return result.response.text();
    } catch (err) {
      console.error("Gemini API call failed, falling back to Groq:", err);
    }
  }

  // Fallback to Groq if Gemini API Key is not set or failed
  console.log("Using Groq fallback for ideation...");
  const groq = await rotationService.getClient();
  const groqMessages: any[] = [
    { role: "system" as any, content: systemPrompt },
    ...messages.map((m: any) => ({
      role: (m.role === "model" || m.role === "ai" || m.role === "assistant" ? "assistant" : "user") as any,
      content: m.content || ""
    }))
  ];

  const modelsToTry = [
    "llama-3.3-70b-versatile",
    "llama-3.1-8b-instant",
    "qwen/qwen3-32b"
  ];

  let lastError: any = null;
  for (const modelName of modelsToTry) {
    try {
      console.log(`[Groq] Attempting completion with model: ${modelName}`);
      let completion = await groq.chat.completions.create({
        model: modelName,
        messages: groqMessages,
        temperature: 0.7
      });

      const finalContent = completion.choices[0]?.message?.content?.trim();
      if (!finalContent) {
        throw new Error(`[Groq] Model ${modelName} returned empty content.`);
      }
      return finalContent;
    } catch (err: any) {
      console.warn(`[Groq] Model ${modelName} failed or rate limited:`, err.message || err);
      lastError = err;
    }
  }

  throw lastError || new Error("All Groq models failed.");
};

// ??$$$ callGeminiValidator accepts brief string, no snapshot serialization needed
export const callGeminiValidator = async (
  brief: string
): Promise<{ approved: boolean; problems: string; suggestion: string }> => {
  const geminiApiKey = process.env.GEMINI_API_KEY;
  const promptText = brief || "No brief provided.";

  if (geminiApiKey) {
    try {
      const genAI = new GoogleGenerativeAI(geminiApiKey);
      const model = genAI.getGenerativeModel({
        model: "gemini-2.5-flash",
        systemInstruction: VALIDATOR_SYSTEM_PROMPT
      });
      const result = await model.generateContent(promptText);
      const raw = result.response.text().trim();
      const clean = raw.replace(/```json|```/g, "").trim();
      return JSON.parse(clean);
    } catch (err) {
      console.error("Validator Gemini call failed:", err);
      // fail open — don't block the user if validator crashes
      return { approved: true, problems: "", suggestion: "" };
    }
  }

  // Groq fallback
  try {
    const groq = await rotationService.getClient();
    const completion = await groq.chat.completions.create({
      model: "llama-3.3-70b-versatile",
      messages: [
        { role: "system", content: VALIDATOR_SYSTEM_PROMPT },
        { role: "user", content: promptText }
      ],
      temperature: 0.3
    });
    const raw = completion.choices[0]?.message?.content?.trim() || "";
    const clean = raw.replace(/```json|```/g, "").trim();
    return JSON.parse(clean);
  } catch (err) {
    console.error("Validator Groq fallback failed:", err);
    return { approved: true, problems: "", suggestion: "" };
  }
};
